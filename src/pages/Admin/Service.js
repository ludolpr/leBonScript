import axios from "axios";

const API_BASE_URL = "http://127.0.0.1:8000/api";
const api = axios.create({
  baseURL: API_BASE_URL,
});
//////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Pour les utilisateurs
//////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////

export const getUsers = () =>
  api
    .get("/users")
    .catch((Erreur) => console.error("Erreur data users:", Erreur));

export const getUser = (id) =>
  api
    .get(`/users/${id}`)
    .catch((Erreur) => console.error("Erreur data user:", Erreur));

export const updateUser = (id, data) =>
  api
    .put(`/users/${id}`, data)
    .catch((Erreur) => console.error("Erreur modifications user:", Erreur));

export const deleteUser = (id) =>
  api
    .delete(`/users/${id}`)
    .catch((Erreur) => console.error("Erreur suppression user:", Erreur));

